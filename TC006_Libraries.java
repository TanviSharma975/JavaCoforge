package Pac1;

public class TC006_Libraries {
	public static void main(String[]args) {
		String num="12345";
		
		int num1=Integer.parseInt(num);//Wrapper class
		System.out.println(num1+1000);
		
		
		
		
		int a=5;//type casting
		float b;
		b=a;
		System.out.println(b);
		////explicitly casting
		int i=(int)5.6;
		System.out.print(i);
		
	}

}
